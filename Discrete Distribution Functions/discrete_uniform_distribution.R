
discrete_uniform<-function(n) {
	return(1/n)
}

#In probability theory and statistics, the discrete uniform distribution is a symmetric probability distribution 
#whereby a finite number of values are equally likely to be observed; every one of n values has equal probability 1/n.
#Another way of saying "discrete uniform distribution" would be "a known, finite number of outcomes equally likely to happen".


n<-readline("Total Values : ")

discrete_uniform(n)
